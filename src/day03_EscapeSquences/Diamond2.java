package day03_EscapeSquences;

public class Diamond2 {
    public static void main(String[] args) {
        System.out.println("\t\t      ^");
        System.out.println("\t\t     / \\");
        System.out.println("\t\t    / * \\");
        System.out.println("\t\t   / * * \\");
        System.out.println("\t\t  / * * * \\");
        System.out.println("\t\t  ---------");
        System.out.println("\t\t  \\ * * * /");
        System.out.println("\t\t   \\ * * /");
        System.out.println("\t\t    \\ * /");
        System.out.println("\t\t     \\ /");
        System.out.println("\t\t      V");

    }
}

